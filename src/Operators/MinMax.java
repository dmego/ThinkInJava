package Operators;
//练习9: 用指数计数法表示double和float类型的最大最小值
public class MinMax {
	public static void main(String[] args) {
		System.out.println("max double:"+java.lang.Double.MAX_VALUE);
		System.out.println("min double:"+java.lang.Double.MIN_VALUE);
		System.out.println("max float:"+java.lang.Float.MAX_VALUE);
		System.out.println("min float:"+java.lang.Float.MIN_VALUE);
	}
}
